    private Block placeRareBlock(Random random) {
        if (random.nextDouble() < 0.15) {
            return NovaCraftBlocks.block_of_vanite;
        } else if (random.nextDouble() >= 0.15 && random.nextDouble() < 0.30) {
            return NovaCraftBlocks.block_of_reinforced_vanite;
        } else if (random.nextDouble() >= 0.30 && random.nextDouble() < 0.45) {
            return Blocks.gold_block;
        } else if (random.nextDouble() >= 0.45 && random.nextDouble() < 0.50) {
            return NovaCraftBlocks.block_of_tophinite;
        } else if (random.nextDouble() >= 0.50 && random.nextDouble() < 0.55) {
            return Blocks.emerald_block;
        } else if (random.nextDouble() >= 0.55 && random.nextDouble() < 0.60) {
            if (Configs.enableTreasureCratesDropDiamonds) {
                return Blocks.diamond_block;
            } else {
                return Blocks.emerald_block;
            }
        } else {
            return Blocks.air;
        }
    }

    private Block placeStoneBricks(Random random) {
        if (random.nextDouble() < 0.65) {
            return NovaCraftBlocks.sculk_bricks;
        } else {
            return NovaCraftBlocks.carved_vanite_bricks;
        }
    }

